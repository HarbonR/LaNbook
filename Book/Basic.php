    <h2 id="Be:_Simple_Present_Tense_(1)">Быть: Настоящее простое время (1)</h2>
    <b>1. Вот несколько примеров глагола be в настоящем простом времени:</b>
    <div class="center-container">
        <div>
            <br>I am a student. (Я студент.)
            <br>He is a teacher. (Он учитель.)
            <br>She is happy. (Она счастлива.)
            <br>We are friends. (Мы друзья.)
            <br>They are at home. (Они дома.)
        </div>
    </div>
    <br><br>
    <b>2. Мы образуем настоящее простое время от be следующим образом:</b>
    <br>
    <div class="center-container">
