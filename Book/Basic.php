    <br><br>
    <b>4. Мы используем быть (Be)</b>
    <br>
    <div class="center-container">
        <div>
            <b>Чтобы сказать, кто мы такие:</b>
            <br>I am a teacher. (Я учитель.)
            <br>You are my friend. (Ты мой друг.)
            <br>He is a doctor. (Он врач.)
            <br>She is an artist. (Она художник.)
            <br>It is a sunny day. (Сегодня солнечный день.)
            <br>We are students. (Мы студенты.)
            <br>They are engineers. (Они инженеры.)
            <br><br>
            <b>Поговорить о погоде:</b>
            <br>It is sunny. (Светит солнце.)
            <br>It is rainy. (Идет дождь.)
            <br>It is cloudy. (Облачно.)
            <br>It is cold. (Холодно.)
            <br>It is hot. (Жарко.)
            <br>It is windy. (Ветреная погода.)
            <br>The weather is nice. (Погода хорошая.)
            <br><br>
            <b>Поговорить о времени:</b>
            <br>It is 10 o'clock. (Сейчас 10 часов.)
            <br>It is morning. (Утро.)
            <br>It is afternoon. (После полудня.)
            <br>It is evening. (Вечер.)
            <br>It is midnight. (Полночь.)
            <br>Today is Friday. (Сегодня пятница.)
            <br>It is the first of September. (Сегодня первое сентября.)
        </div>
