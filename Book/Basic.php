    <h2 id="Be:_Simple_Present_Tense_(2)">Быть: Настоящее простое время (2)</h2>

    <b>1. Мы используем быть (Be)</b>
    <br>
    <div class="center-container">
        <div>
            <b>Говорить о том, что мы чувствуем</b>
            <br>I am happy. (Я счастлив.)
            <br>She is sad. (Она грустная.)
            <br>They are excited. (Они взволнованы.)
            <br>He is nervous. (Он нервничает.)
            <br>We are tired. (Мы устали.)
        </div>
    </div>
    <br>
    <div class="center-container">
        <div>
            <b>Приветствовать людей</b>
            <br>Hello, how are you? (Привет, как ты?)
            <br>Good morning, everyone! (Доброе утро, всем!)
            <br>Hi, I am John. (Привет, я Джон.)
            <br>She is our new colleague. (Она наша новая коллега.)
            <br>We are happy to see you. (Мы рады вас видеть.)
        </div>
    </div>
    <br>
    <b>2. Мы используем there + be, чтобы сказать о существовании чего-либо. There + be может использоваться для разговора о том, где что находится</b>
    <div class="center-container">
        <div>
            <br>There is a book on the table. (На столе есть книга.)
            <br>There are two cats in the garden. (В саду есть две кошки.)
            <br>There is a restaurant nearby. (Рядом есть ресторан.)
            <br>There are many stars in the sky. (В небе много звезд.)
            <br>There is a meeting at 3 PM. (В 3 часа встреча.)
        </div>
    </div>
    <br>
    <b>Мы также используем there + be, когда говорим о том, что происходит</b>
    <div class="center-container">
        <div>
            <br>There is a concert tonight. (Сегодня вечером концерт.)
            <br>There are meetings every Monday. (Каждый понедельник проходят встречи.)
            <br>There is a workshop happening right now. (Сейчас проходит мастер-класс.)
            <br>There are several events planned for this weekend. (На эти выходные запланировано несколько мероприятий.)
            <br>There is a movie playing at the theater. (В кинотеатре идет фильм.)
        </div>
    </div>
    <br>
    <b>3. Мы формируем вопрос с be в настоящем простом времени следующим образом</b>
